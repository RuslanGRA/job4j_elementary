package ru.job4j.math;

public class MathFunction {
    public static double sum(double first, double second) {
        return first + second;
    }

    public static double myltiply(double first, double second) {
       return first * second;
    }

    public static double less(double first, double second) {
        return first - second;
    }

    public static double share(double first, double second) {
        return first / second;
    }
}

